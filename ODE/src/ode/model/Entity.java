//*************************************************************************************************
package ode.model;
//*************************************************************************************************

//*************************************************************************************************
public class Entity {

	//=============================================================================================
	private final Model model;
	//=============================================================================================

	//=============================================================================================
	Entity(Model model) {
		this.model = model;
	}
	//=============================================================================================

	//=============================================================================================
	public CameraData getCameraData() {
		return model.cameraDataMap.get(this);
	}
	//=============================================================================================
	
	//=============================================================================================
	public KineticData getKineticData() {
		return model.kineticDataMap.get(this);
	}
	//=============================================================================================

	//=============================================================================================
	public PoseData getPoseData() {
		return model.poseDataMap.get(this);
	}
	//=============================================================================================

}
//*************************************************************************************************
