package ode.model;
//*************************************************************************************************

import javax.vecmath.Quat4f;
import javax.vecmath.Vector3f;

//*************************************************************************************************
public class KineticData {
	
	//=============================================================================================
	public final Vector3f velocity = new Vector3f();
	public final Quat4f   rotation = new Quat4f(0, 0, 0, 1);
	//=============================================================================================

	//=============================================================================================
	public KineticData() {
		velocity.set(0, 0, 0);
		rotation.set(0, 0, 0, 1);
	}
	//=============================================================================================
	
}
