//*************************************************************************************************
package ode.overlay.gui;
//*************************************************************************************************

import ode.util.Dimension;
import ode.util.FloatMargin;

//*************************************************************************************************
public class Layout {

	//=============================================================================================
	public Dimension minSize = Dimension.DEFAULT_MIN;
	public Dimension maxSize = Dimension.DEFAULT_MAX;
	//=============================================================================================

	//=============================================================================================
	public FloatMargin margin = FloatMargin.MARGIN_5;
	public FloatMargin border = FloatMargin.MARGIN_1;
	public FloatMargin padding = FloatMargin.MARGIN_2;
	//=============================================================================================

	//=============================================================================================
	public Layouter layouter = null;
	//=============================================================================================
	
	//=============================================================================================
	public void layout(Widget widget) {
		if (layouter != null) layouter.layout(widget);
	}
	//=============================================================================================
	
}
//*************************************************************************************************

