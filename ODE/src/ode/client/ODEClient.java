//*************************************************************************************************
package ode.client;

import ode.gui.ODEWidgets;
import ode.platform.ODEPlatform;

//*************************************************************************************************

//*************************************************************************************************
public class ODEClient {

	//=============================================================================================
	private final ODEPlatform platform;
	private final ODEWidgets widgets;
	//=============================================================================================

	//=============================================================================================
	public ODEClient() {
		platform = new ODEPlatform();
		widgets = new ODEWidgets();
		platform.assign(widgets);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void configure(String[] args) {
		configurePlatform();
	}
	//=============================================================================================

	//=============================================================================================
	private void configurePlatform() {
		platform.title("ODE Client Window");
		platform.size(800, 600);
		platform.maximized(true);
	}
	//=============================================================================================
	
	//=============================================================================================
	private void configureWidgets() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public void run() {
		platform.displayed(true);
		boolean terminate = false;
		while (!terminate) {
			platform.render();
			Thread.yield();
		}
	}
	//=============================================================================================
	
	//=============================================================================================
	public static void main(String[] args) {
		ODEClient client = new ODEClient();
		client.configure(args);
		client.run();
	}
	//=============================================================================================

}
//*************************************************************************************************
