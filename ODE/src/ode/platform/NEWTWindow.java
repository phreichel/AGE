//*************************************************************************************************
package ode.platform;
//*************************************************************************************************

import com.jogamp.newt.opengl.GLWindow;
import com.jogamp.opengl.GLCapabilities;
import com.jogamp.opengl.GLProfile;

//*************************************************************************************************
class NEWTWindow {

	//=============================================================================================
	private GLWindow window;
	//=============================================================================================

	//=============================================================================================
	public NEWTWindow() {
		init();
	}
	//=============================================================================================
	
	//=============================================================================================
	public void init() {
		GLProfile glProfile = GLProfile.getDefault();
		GLCapabilities glCaps = new GLCapabilities(glProfile);
		window = GLWindow.create(glCaps);
		window.setSize(800, 600);
		window.setTitle("ODE Test Window");
		window.setMaximized(true, true);
		window.setFullscreen(false);
	}
	//=============================================================================================

	//=============================================================================================
	public void register(NEWTInputAdapter adapter) {
		window.addKeyListener(adapter);
		window.addMouseListener(adapter);
	}
	//=============================================================================================

	//=============================================================================================
	public void register(NEWTGraphicsAdapter adapter) {
		window.addGLEventListener(adapter);
	}
	//=============================================================================================
	
	//=============================================================================================
	public String getTitle() {
		return window.getTitle();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setTitle(String title) {
		window.setTitle(title);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean isMaximized() {
		return window.isMaximizedHorz();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setMaximized(boolean maximized) {
		window.setMaximized(maximized, maximized);
	}
	//=============================================================================================
	
	//=============================================================================================
	public boolean isFullscreen() {
		return window.isFullscreen();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setFullscreen(boolean fullscreen) {
		window.setFullscreen(fullscreen);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean isVisible() {
		return window.isVisible();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setVisible(boolean visible) {
		window.setVisible(visible);
	}
	//=============================================================================================

	//=============================================================================================
	public void update() {
		window.display();
	}
	//=============================================================================================
	
}
//*************************************************************************************************
