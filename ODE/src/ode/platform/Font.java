//*************************************************************************************************
package ode.platform;
//*************************************************************************************************

import com.jogamp.opengl.util.awt.TextRenderer;

//*************************************************************************************************
public class Font {

	//=============================================================================================
	private java.awt.Font awtFont;
	private TextRenderer textRenderer;
	//=============================================================================================

	//=============================================================================================
	protected Font(java.awt.Font awtFont) {
		this.awtFont = awtFont;
		textRenderer = new TextRenderer(awtFont, true);
	}
	//=============================================================================================
	
	//=============================================================================================
	protected Font(String name, float size, FontDecoration decoration) {
		String fontText = String.format("%s-%.0f-%s", name, size, decoration);
		awtFont = java.awt.Font.decode(fontText);
		textRenderer = new TextRenderer(awtFont, true);
	}
	//=============================================================================================
	
	//=============================================================================================
	protected TextRenderer getRenderer() {
		return textRenderer;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
