//*************************************************************************************************
package ode.platform;
//*************************************************************************************************

import ode.event.Events;

//*************************************************************************************************
public class Platform {

	//=============================================================================================
	private NEWTWindow newtWindow = new NEWTWindow();
	private NEWTInputAdapter newtInputAdapter = new NEWTInputAdapter();
	private NEWTGraphicsAdapter newtGraphicsAdapter = new NEWTGraphicsAdapter(); 
	//=============================================================================================

	//=============================================================================================
	public Platform(Events events) {
		newtInputAdapter.register(events);
		newtWindow.register(newtInputAdapter);
		newtWindow.register(newtGraphicsAdapter);
	}
	//=============================================================================================
	
	//=============================================================================================
	public String getTitle() {
		return newtWindow.getTitle();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setTitle(String title) {
		newtWindow.setTitle(title);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean isMaximized() {
		return newtWindow.isMaximized();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setMaximized(boolean maximized) {
		newtWindow.setMaximized(maximized);
	}
	//=============================================================================================
	
	//=============================================================================================
	public boolean isFullscreen() {
		return newtWindow.isFullscreen();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setFullscreen(boolean fullscreen) {
		newtWindow.setFullscreen(fullscreen);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean isVisible() {
		return newtWindow.isVisible();
	}
	//=============================================================================================	
	
	//=============================================================================================
	public void setVisible(boolean visible) {
		newtWindow.setVisible(visible);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void update() {
		newtWindow.update();
	}
	//=============================================================================================

}
//*************************************************************************************************
