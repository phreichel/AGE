//*************************************************************************************************
package ode.msg;
//*************************************************************************************************

//*************************************************************************************************
public class KeyData {

	//=============================================================================================
	private Key key;
	private char keyChar;
	//=============================================================================================

	//=============================================================================================
	public Key key() {
		return key;
	}
	//=============================================================================================

	//=============================================================================================
	public char keyChar() {
		return keyChar;
	}
	//=============================================================================================
	
	//=============================================================================================
	void set(Key key, char keyChar) {
		this.key = key;
		this.keyChar = keyChar;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
