package ode.event;
//*************************************************************************************************

//*************************************************************************************************
public class Event {

	//=============================================================================================
	private static int inc = 0;
	//=============================================================================================
	
	//=============================================================================================
	public static final int NONE  = inc++;
	public static final int MOUSE = inc++;
	//=============================================================================================

	//=============================================================================================
	public int type = NONE;
	public Object[] data = null;
	//=============================================================================================
	
	//=============================================================================================
	public void clear() {
		set(NONE, (Object[]) null);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(int type, Object ... elements) {
		this.type = type;
		this.data = elements;
	}
	//=============================================================================================
		
}
