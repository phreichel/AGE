//*************************************************************************************************
package ode.event;
//*************************************************************************************************

//*************************************************************************************************
public class PointerData {

	//=============================================================================================
	private static int inc = 0;
	//=============================================================================================

	//=============================================================================================
	/**
	 * represents "No Button"
	 */
	public static final int NONE = inc++;
	//=============================================================================================
	public static final int BUTTON1 = inc++;
	public static final int BUTTON2 = inc++;
	public static final int BUTTON3 = inc++;
	public static final int BUTTON4 = inc++;
	public static final int BUTTON5 = inc++;
	public static final int BUTTON6 = inc++;
	//=============================================================================================

	//=============================================================================================
	public PointerData() {
		clear();
	}
	//=============================================================================================

	//=============================================================================================
	public PointerData(float x, float y) {
		set(x, y);
	}
	//=============================================================================================

	//=============================================================================================
	public PointerData(float x, float y, int button, int count) {
		set(x, y, button, count);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void clear() {
		set(0, 0, NONE, 0);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float x, float y) {
		set(x, y, NONE, 0);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float x, float y, int button, int count) {
		this.x = x;
		this.y = y;
		this.button = button;
		this.count = count;
		this.wheel = 0f;
	}
	//=============================================================================================
	
	//=============================================================================================
	public float x;
	public float y;
	public int button;
	public int count;
	public float wheel;
	//=============================================================================================

}
//*************************************************************************************************
