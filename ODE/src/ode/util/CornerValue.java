package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class CornerValue {

	//=============================================================================================
	public float bottomLeft, bottomRight, topLeft, topRight;
	//=============================================================================================

	//=============================================================================================
	public CornerValue() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public CornerValue(CornerValue m) {
		set(m);
	}
	//=============================================================================================
	
	//=============================================================================================
	public CornerValue(float bl, float br, float tl, float tr) {
		set(bl, br, tl, tr);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(CornerValue m) {
		bottomLeft = m.bottomLeft;
		bottomRight = m.bottomRight;
		topLeft = m.topLeft;
		topRight = m.topRight;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float bl, float br, float tl, float tr) {
		this.bottomLeft = bl;
		this.bottomRight = br;
		this.topLeft = tl;
		this.topRight = tr;
	}
	//=============================================================================================

}
//*************************************************************************************************