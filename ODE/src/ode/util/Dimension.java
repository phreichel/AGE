package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class Dimension {

	//=============================================================================================
	public static final Dimension DEFAULT_MIN = new Dimension(10, 10);
	public static final Dimension DEFAULT_MAX= new Dimension(Float.POSITIVE_INFINITY, Float.POSITIVE_INFINITY);	
	//=============================================================================================
	
	//=============================================================================================
	public float width, height;
	//=============================================================================================

	//=============================================================================================
	public Dimension() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Dimension(float w, float h) {
		set(w, h);
	}
	//=============================================================================================

	//=============================================================================================
	public Dimension(Dimension d) {
		set(d);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(Dimension d) {
		width = d.width;
		height = d.height;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float w, float h) {
		width = w;
		height = h;
	}
	//=============================================================================================
	
}
