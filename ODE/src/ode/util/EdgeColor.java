package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class EdgeColor {

	//=============================================================================================
	public final Color left = new Color();
	public final Color right = new Color();
	public final Color bottom = new Color();
	public final Color top = new Color();
	//=============================================================================================

	//=============================================================================================
	public EdgeColor() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public EdgeColor(EdgeColor m) {
		set(m);
	}
	//=============================================================================================
	
	//=============================================================================================
	public EdgeColor(Color left, Color right, Color bottom, Color top) {
		set(left, right, bottom, top);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(EdgeColor m) {
		left.set(m.left);
		right.set(m.right);
		bottom.set(m.bottom);
		top.set(m.top);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color left, Color right, Color bottom, Color top) {
		this.left.set(left);
		this.right.set(right);
		this.bottom.set(bottom);
		this.top.set(top);
	}
	//=============================================================================================

}
//*************************************************************************************************