//*************************************************************************************************
public class FloatMargin {

	//=============================================================================================
	public static final FloatMargin MARGIN_1 = new FloatMargin(1, 1, 1, 1);
	public static final FloatMargin MARGIN_2 = new FloatMargin(2, 2, 2, 2);
	public static final FloatMargin MARGIN_5 = new FloatMargin(5, 5, 5, 5);
	public static final FloatMargin MARGIN_10 = new FloatMargin(10, 10, 10, 10);
	//=============================================================================================
	
	//=============================================================================================
	public float left, right;
	public float top, bottom;
	//=============================================================================================

	//=============================================================================================
	public FloatMargin() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public FloatMargin(FloatMargin m) {
		set(m);
	}
	//=============================================================================================
	
	//=============================================================================================
	public FloatMargin(float left, float right, float top, float bottom) {
		set(left, right, top, bottom);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(FloatMargin m) {
		left = m.left;
		right = m.right;
		top = m.top;
		bottom = m.bottom;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float left, float right, float top, float bottom) {
		this.left = left;
		this.right = right;
		this.top = top;
		this.bottom = bottom;
	}
	//=============================================================================================

}
//*************************************************************************************************