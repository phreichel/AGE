package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class CornerColor {

	//=============================================================================================
	public final Color bottomLeft = new Color();
	public final Color bottomRight = new Color();
	public final Color topLeft = new Color();
	public final Color topRight = new Color();
	//=============================================================================================

	//=============================================================================================
	public CornerColor() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public CornerColor(CornerColor m) {
		set(m);
	}
	//=============================================================================================
	
	//=============================================================================================
	public CornerColor(Color bl, Color br, Color tl, Color tr) {
		set(bl, br, tl, tr);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(CornerColor m) {
		bottomLeft.set(m.bottomLeft);
		bottomRight.set(m.bottomRight);
		topLeft.set(m.topLeft);
		topRight.set(m.topRight);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color bl, Color br, Color tl, Color tr) {
		this.bottomLeft.set(bl);
		this.bottomRight.set(br);
		this.topLeft.set(tl);
		this.topRight.set(tr);
	}
	//=============================================================================================

}
//*************************************************************************************************