//*************************************************************************************************
package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class Margin {

	//=============================================================================================
	public float left = 0f;
	public float right = 0f;
	public float bottom = 0f;
	public float top = 0f;
	//=============================================================================================

	//=============================================================================================
	public Margin() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Margin(Margin margin) {
		set(margin);
	}
	//=============================================================================================

	//=============================================================================================
	public Margin(float left, float right, float bottom, float top) {
		set(left, right, bottom, top);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Margin margin) {
		left = margin.left;
		right = margin.right;
		bottom = margin.bottom;
		top = margin.top;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float left, float right, float bottom, float top) {
		this.left = left;
		this.right = right;
		this.bottom = bottom;
		this.top = top;
	}
	//=============================================================================================
	
}
//*************************************************************************************************