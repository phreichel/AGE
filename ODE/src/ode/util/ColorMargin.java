//*************************************************************************************************
package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class ColorMargin {

	//=============================================================================================
	public final Color left = new Color();
	public final Color right = new Color();
	public final Color top = new Color();
	public final Color bottom = new Color();
	//=============================================================================================

	//=============================================================================================
	public ColorMargin() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public ColorMargin(ColorMargin cm) {
		set(cm);
	}
	//=============================================================================================

	//=============================================================================================
	public ColorMargin(Color left, Color right, Color top, Color bottom) {
		set(left, right, top, bottom);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(ColorMargin cm) {
		this.left.set(cm.left);
		this.right.set(cm.right);
		this.top.set(cm.top);
		this.bottom.set(cm.bottom);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color left, Color right, Color top, Color bottom) {
		this.left.set(left);
		this.right.set(right);
		this.top.set(top);
		this.bottom.set(bottom);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
