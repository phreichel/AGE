//*************************************************************************************************
package ode.util;
//*************************************************************************************************

import java.util.Objects;

//*************************************************************************************************
public class Color {

	//=============================================================================================
	public float red;
	public float green;
	public float blue;
	public float alpha;
	//=============================================================================================

	//=============================================================================================
	public Color() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Color(Color c) {
		set(c);
	}
	//=============================================================================================

	//=============================================================================================
	public Color(float r, float g, float b, float a) {
		set(r, g, b, a);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color c) {
		red = c.red;
		green = c.green;
		blue = c.blue;
		alpha = c.alpha;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float r, float g, float b, float a) {
		red = r;
		green = g;
		blue = b;
		alpha = a;
	}
	//=============================================================================================

	//=============================================================================================
	public int hashCode() {
		return Objects.hash(alpha, blue, green, red);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Color other = (Color) obj;
		return Float.floatToIntBits(alpha) == Float.floatToIntBits(other.alpha)
				&& Float.floatToIntBits(blue) == Float.floatToIntBits(other.blue)
				&& Float.floatToIntBits(green) == Float.floatToIntBits(other.green)
				&& Float.floatToIntBits(red) == Float.floatToIntBits(other.red);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
