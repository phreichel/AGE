//*************************************************************************************************
package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class Color {

	//=============================================================================================
	public float red;
	public float green;
	public float blue;
	public float alpha;
	//=============================================================================================

	//=============================================================================================
	public Color() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Color(Color c) {
		set(c);
	}
	//=============================================================================================

	//=============================================================================================
	public Color(float r, float g, float b, float a) {
		set(r, g, b, a);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color c) {
		this.red = c.red;
		this.green = c.green;
		this.blue = c.blue;
		this.alpha = c.alpha;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float r, float g, float b, float a) {
		this.red = r;
		this.green = g;
		this.blue = b;
		this.alpha = a;
	}
	//=============================================================================================
	
}
//*************************************************************************************************