//*************************************************************************************************
package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class Color {

	//=============================================================================================
	public float red;
	public float green;
	public float blue;
	public float alpha;
	//=============================================================================================

	//=============================================================================================
	public Color() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Color(Color c) {
		set(c);
	}
	//=============================================================================================

	//=============================================================================================
	public Color(float r, float g, float b, float a) {
		set(r, g, b, a);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(Color c) {
		red = c.red;
		green = c.green;
		blue = c.blue;
		alpha = c.alpha;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float r, float g, float b, float a) {
		red = r;
		green = g;
		blue = b;
		alpha = a;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
