package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class EdgeValue {

	//=============================================================================================
	public float left, right, bottom, top;
	//=============================================================================================

	//=============================================================================================
	public EdgeValue() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public EdgeValue(EdgeValue m) {
		set(m);
	}
	//=============================================================================================
	
	//=============================================================================================
	public EdgeValue(float left, float right, float bottom, float top) {
		set(left, right, bottom, top);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(EdgeValue m) {
		left = m.left;
		right = m.right;
		bottom = m.bottom;
		top = m.top;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float left, float right, float bottom, float top) {
		this.left = left;
		this.right = right;
		this.bottom = bottom;
		this.top = top;
	}
	//=============================================================================================

}
//*************************************************************************************************