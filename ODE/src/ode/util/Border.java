//*************************************************************************************************
package ode.util;
//*************************************************************************************************

//*************************************************************************************************
public class Border {

	//=============================================================================================
	public final FloatMargin size = new FloatMargin();
	public final ColorMargin color = new ColorMargin();
	//=============================================================================================

	//=============================================================================================
	public Border() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public Border(Border b) {
		set(b);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Border(FloatMargin size, ColorMargin color) {
		set(size, color);
	}
	//=============================================================================================

	//=============================================================================================
	public void set(Border b) {
		size.set(b.size);
		color.set(b.color);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(FloatMargin size, ColorMargin color) {
		this.size.set(size);
		this.color.set(color);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
