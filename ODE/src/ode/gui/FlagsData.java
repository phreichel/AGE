import java.util.BitSet;

//*************************************************************************************************
public class FlagsData {

	//=============================================================================================
	private static int inc = 0;
	//=============================================================================================

	//=============================================================================================
	public static final int DISPLAYED = inc++;
	public static final int HOVER = inc++;
	//=============================================================================================

	//=============================================================================================
	public final BitSet flags = new BitSet(inc);
	//=============================================================================================

	//=============================================================================================
	public FlagsData() {
		flags.set(DISPLAYED);
	}
	//=============================================================================================
	
}
