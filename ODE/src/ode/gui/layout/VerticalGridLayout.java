//*************************************************************************************************
package ode.gui.layout;

import ode.gui.Widget;
import ode.util.Dimension;

//*************************************************************************************************

//*************************************************************************************************
public class VerticalGridLayout extends Layout {

	//=============================================================================================
	private final Dimension gridSize = new Dimension();
	//=============================================================================================

	//=============================================================================================
	public VerticalGridLayout() {
		
	}
	//=============================================================================================

	//=============================================================================================
	public VerticalGridLayout(Dimension gridSize) {
		setGridSize(gridSize);
	}
	//=============================================================================================

	//=============================================================================================
	public VerticalGridLayout(float w, float h) {
		setGridSize(w, h);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Dimension getGridSize() {
		return gridSize;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void setGridSize(Dimension gridSize) {
		this.gridSize.set(gridSize);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void setGridSize(float width, float height) {
		gridSize.set(width, height);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void apply(Widget widget) {
		float xpos = 0f;
		float ypos = 0f;
		float width = gridSize.width;
		float height = gridSize.height;
		for (Widget child : widget.getChildren()) {
			child.setLocation(xpos, ypos);
			child.setSize(width, height);
			ypos += child.getHeight();
		}
		widget.setSize(width, ypos);
	}
	//=============================================================================================

}
//*************************************************************************************************
