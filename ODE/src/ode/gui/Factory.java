//*************************************************************************************************
package ode.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Factory {

	//=============================================================================================
	private GUI gui;
	//=============================================================================================

	//=============================================================================================
	public Factory(GUI gui) {
		this.gui = gui;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newLabel(String text) {
		Type type = Type.LABEL;
		return gui.newBuilder()
			.type(type)
			.set(Flag.DISPLAYED)
			.text(text)
			.style(gui.theme().get(type))
			.dimension(150, 20);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newButton(String text) {
		Type type = Type.BUTTON;
		return gui.newBuilder()
			.type(type)
			.set(Flag.DISPLAYED)
			.text(text)
			.style(gui.theme().get(type))
			.dimension(100, 20);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newTextField(String text) {
		Type type = Type.TEXTFIELD;
		return gui.newBuilder()
			.type(type)
			.set(Flag.DISPLAYED)
			.text(text)
			.style(gui.theme().get(type))
			.dimension(200, 20);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newGroup() {
		Type type = Type.GROUP;
		return gui.newBuilder()
			.type(type)
			.set(Flag.DISPLAYED)
			.style(gui.theme().get(type))
			.dimension(300, 800);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
