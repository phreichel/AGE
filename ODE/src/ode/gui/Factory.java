//*************************************************************************************************
package ode.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Factory {

	//=============================================================================================
	private GUI gui;
	//=============================================================================================

	//=============================================================================================
	public Factory(GUI gui) {
		this.gui = gui;
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newWidget() {
		return gui.newBuilder()
			.set(Flag.DISPLAYED);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newLabel(String text) {
		return newWidget()
			.text(text)
			.foreground(0, 0, 0, 0)
			.background(0, 0, 0, 0);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newFrame() {
		return newWidget()
			.set(Flag.REACTIVE)
			.set(Flag.LAYERABLE)
			.foreground(1, .8f, 0, 1)
			.background(.6f, .6f, .6f, 1);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newBox() {
		return newWidget()
			.foreground(1, 1, 1, 1)
			.background(.8f, .8f, .8f, 1);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newButton(String text) {	
		return newButton(null, text);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newButton(
			String icon,
			String text) {
		return newWidget()
			.set(Flag.REACTIVE)
			.set(Flag.ACTION_HOVER)
			.foreground(1, .8f, 0, 1)
			.background(.4f, .4f, 1f, 1)
			.icon(icon)
			.dimension(120, 20)
			.child(
				newLabel(text)
				.font("ode.font.title")
				.position(3, 17)
				.widget()
			);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newToolButton(String caption) {
		return newButton(caption)
			.font("ode.font.title")
			.dimension(20, 20);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newTitlebar(String caption) {
		return newBox()
			.set(Flag.REACTIVE)
			.foreground(1, 1, 1, 1)
			.background(.2f, .2f, .6f, 1)
			.dimension(20, 120)
			.child(
				newLabel(caption)
				.position(3, 17)
				.font("ode.font.title")
				.widget()
			);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Builder newWindow(String caption, float w, float h) {
		return newFrame()
			.layout("window")
			.dimension(w, h)
			.child(
				newTitlebar(caption)
					.set(Flag.DIRTY)
					.set(Flag.ACTION_HOVER)
					.set(Flag.ACTION_PARENT_MOVE)
					.position(5, 5)
					.widget(),
				newToolButton("+")
					.set(Flag.ACTION_PARENT_RESIZE)
					.widget(),
				newToolButton("X")
					.action(Actions.CLOSE_PARENT)
					.widget(),
				newBox()
					.position(5, 30)
					.widget()
			);
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newVerticalBox() {
		return newBox()
			.layout("vertical");
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newHorizontalBox() {
		return newBox()
			.layout("horizontal");
	}
	//=============================================================================================

	//=============================================================================================
	public Builder newTextbox() {
		return newWidget()
			.dimension(200, 20)
			.background(0, 0, 0, 1)
			.foreground(1, 1, 1, 1)
			.child(
				newLabel("TEST gtet|")
				.font("ode.font.bold")
				.position(3, 14)
				.widget()
			);			
	}
	//=============================================================================================
	
}
//*************************************************************************************************
