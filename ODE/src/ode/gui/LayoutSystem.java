//*************************************************************************************************
package ode.gui;
//*************************************************************************************************

import java.util.Map;
import java.util.Map.Entry;

//*************************************************************************************************
public class LayoutSystem {

	//=============================================================================================
	private Map<Widget, LayoutData> layoutMap;
	//=============================================================================================

	//=============================================================================================
	public LayoutSystem(Map<Widget, LayoutData> layoutMap) {
		this.layoutMap = layoutMap;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void update() {
		for (Entry<Widget, LayoutData> entry : layoutMap.entrySet()) {
			Widget widget = entry.getKey();
			LayoutData layoutData = entry.getValue();
			layoutData.action.perform(widget);
		}
	}
	//=============================================================================================
	
}
