package ode.gui;
//*************************************************************************************************

import java.util.HashMap;
import java.util.Map;

//*************************************************************************************************
public class Theme {

	//=============================================================================================
	private Style base;
	private final Map<Type, Style> styles = new HashMap<>();
	//=============================================================================================

	//=============================================================================================
	public Style get(Type type) {
		Style style= styles.get(type);
		if (style == null) style = base;
		return style;
	}
	//=============================================================================================
	
}
