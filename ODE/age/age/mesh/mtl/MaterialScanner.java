package age.mesh.mtl;
//*************************************************************************************************

import age.util.Scanner;

//*************************************************************************************************
public class MaterialScanner extends Scanner {

	//=============================================================================================
	protected boolean isKeyword(String s) {
		return
			s.equals("Ns") ||
			s.equals("Ni") ||
			s.equals("d")  ||
			s.equals("Tr") ||
			s.equals("Ka") ||
			s.equals("Kd") ||
			s.equals("Ks") ||
			s.equals("Ke") ||
			s.equals("illum") ||
			s.equals("map_Ns") ||
			s.equals("map_Kd") ||
			s.equals("map_Bump") ||
			s.equals("newmtl");
	}
	//=============================================================================================
	
}
