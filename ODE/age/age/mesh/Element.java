//*************************************************************************************************
package age.mesh;
//*************************************************************************************************

//*************************************************************************************************
public class Element {

	//=============================================================================================
	private final Mesh mesh;
	//=============================================================================================

	//=============================================================================================
	private int index = 0;
	//=============================================================================================
	
	//=============================================================================================
	Element(Mesh mesh) {
		this.mesh = mesh;
	}
	//=============================================================================================

	//=============================================================================================
	Element set(int idx) {
		this.index = idx;
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public ElementType type() {
		return mesh.types[index];
	}
	//=============================================================================================

	//=============================================================================================
	public int[] indices() {
		return mesh.indices[index];
	}
	//=============================================================================================

	//=============================================================================================
	public float[] colors() {
		return mesh.colors;
	}
	//=============================================================================================

	//=============================================================================================
	public float[] normals() {
		return mesh.normals;
	}
	//=============================================================================================

	//=============================================================================================
	public float[] textures() {
		return mesh.textures;
	}
	//=============================================================================================

	//=============================================================================================
	public float[] vertices() {
		return mesh.vertices;
	}
	//=============================================================================================

	//=============================================================================================
	public boolean hasColors() {
		return mesh.layers.contains(MeshLayer.COLORS);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean hasNormals() {
		return mesh.layers.contains(MeshLayer.NORMALS);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean hasTextures() {
		return mesh.layers.contains(MeshLayer.TEXTURES);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
