//*************************************************************************************************
package age.scene;
//*************************************************************************************************

import age.port.Port;

//*************************************************************************************************
//TODO:add javadoc comments
public class Scene {

	//=============================================================================================
	private Node root = new Node();
	private Node camera = null;
	//=============================================================================================

	//=============================================================================================
	private Rendering rendering = new Rendering(this);
	//=============================================================================================
	
	//=============================================================================================
	public void assign(Port port) {
		port.add(rendering);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Node root() {
		return root;
	}
	//=============================================================================================

	//=============================================================================================
	public Node camera() {
		return camera;
	}
	//=============================================================================================

	//=============================================================================================
	public void camera(Node camera) {
		this.camera = camera;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
