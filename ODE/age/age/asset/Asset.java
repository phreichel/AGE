//*************************************************************************************************
package age.asset;

import java.util.UUID;

//*************************************************************************************************

//*************************************************************************************************
public class Asset {

	//=============================================================================================
	public final UUID uuid;
	public final String name;
	public final int version;
	//=============================================================================================

	//=============================================================================================
	public Asset(
			String name,
			int version) {
		this.uuid = UUID.randomUUID();
		this.name = name;
		this.version = version;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Asset(
			UUID uuid,
			String name,
			int version) {
		this.uuid = uuid;
		this.name = name;
		this.version = version;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
