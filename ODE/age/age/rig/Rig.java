//*************************************************************************************************
package age.rig;
//*************************************************************************************************

//*************************************************************************************************
public class Rig {

	//=============================================================================================
	private final Skeleton skeleton;
	private float scale = 1f;
	private float mark  = 0f;
	//=============================================================================================

	//=============================================================================================
	public Rig(Skeleton skeleton) {
		this.skeleton = skeleton;
	}
	//=============================================================================================

	//=============================================================================================
	public Skeleton skeleton() {
		return skeleton;
	}
	//=============================================================================================
	
	//=============================================================================================
	public float scale() {
		return scale;
	}
	//=============================================================================================

	//=============================================================================================
	public void scale(float scale) {
		this.scale = scale;
	}
	//=============================================================================================
	
	//=============================================================================================
	public float mark() {
		return mark;
	}
	//=============================================================================================

	//=============================================================================================
	public void mark(float mark) {
		this.mark = (mark + (float) Math.floor(mark)) % 1f;
	}
	//=============================================================================================

	//=============================================================================================
	public void addMark(float dT) {
		mark(this.mark + dT * this.scale);
	}
	//=============================================================================================
	
	//=============================================================================================
	public float time() {
		return mark * skeleton.time();
	}
	//=============================================================================================
	
}
//*************************************************************************************************
