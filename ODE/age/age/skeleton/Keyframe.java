//*************************************************************************************************
package age.skeleton;
//*************************************************************************************************

import javax.vecmath.AxisAngle4f;
import javax.vecmath.Matrix3f;
import javax.vecmath.Quat4f;
import javax.vecmath.Vector3f;

//*************************************************************************************************
public class Keyframe {

	//=============================================================================================
	private float time;
	//=============================================================================================

	//=============================================================================================
	private final Vector3f translation = new Vector3f();
	private final Quat4f orientation = new Quat4f(0, 0, 0, 1);
	//=============================================================================================

	//=============================================================================================
	public float time() {
		return time;
	}
	//=============================================================================================

	//=============================================================================================
	public void time(float time) {
		this.time = time;
	}
	//=============================================================================================

	//=============================================================================================
	public Vector3f translation() {
		return translation;
	}
	//=============================================================================================

	//=============================================================================================
	public void translation(Vector3f translation) {
		this.translation.set(translation);
	}
	//=============================================================================================

	//=============================================================================================
	public void translation(float x, float y, float z) {
		this.translation.set(x, y, z);
	}
	//=============================================================================================

	//=============================================================================================
	public void addTranslation(float dx, float dy, float dz) {
		this.translation.x += dx;
		this.translation.y += dy;
		this.translation.z += dz;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Quat4f orientation() {
		return orientation;
	}
	//=============================================================================================

	//=============================================================================================
	public void orientation(Quat4f ot) {
		this.orientation.set(ot);
	}
	//=============================================================================================

	//=============================================================================================
	public void addOrientation(Quat4f dot) {
		this.orientation.mul(dot, this.orientation);
	}
	//=============================================================================================

	//=============================================================================================
	public void addOrientation(Vector3f n, float a) {
		a = (float) Math.toRadians(a);
		AxisAngle4f aa = new AxisAngle4f(n, a);
		Quat4f q = new Quat4f();
		q.set(aa);
		addOrientation(q);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void addOrientation(float nx, float ny, float nz, float a) {
		Vector3f n = new Vector3f(nx, ny, nz);
		n.normalize();
		addOrientation(n, a);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void orientation(Matrix3f dot) {
		this.orientation.set(dot);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
