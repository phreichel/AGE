//*************************************************************************************************
package age.skeleton;
//*************************************************************************************************

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

//*************************************************************************************************
public class Skeleton {

	//=============================================================================================
	private float      speed = 1f;
	private float      time  = 0f;
	private float      mark  = 0f;
	private List<Bone> bones = new ArrayList<>();
	private List<Bone> bones_ro = Collections.unmodifiableList(bones);
	//=============================================================================================

	//=============================================================================================
	public void bone(Bone bone) {
		bones.add(bone);
	}
	//=============================================================================================

	//=============================================================================================
	public Bone bone(String name) {
		Bone bone = new Bone();
		bone.name(name);
		bones.add(bone);
		return bone;
	}
	//=============================================================================================
	
	//=============================================================================================
	public List<Bone> bones() {
		return bones_ro;
	}
	//=============================================================================================

	//=============================================================================================
	public float speed() {
		return speed;
	}
	//=============================================================================================

	//=============================================================================================
	public void speed(float speed) {
		this.speed = speed;
	}
	//=============================================================================================
	
	//=============================================================================================
	public float time() {
		return time;
	}
	//=============================================================================================

	//=============================================================================================
	public void time(float time) {
		this.time = time;
	}
	//=============================================================================================

	//=============================================================================================
	public float mark() {
		return mark;
	}
	//=============================================================================================

	//=============================================================================================
	public void mark(float mark) {
		this.mark = ((mark * speed) % time) / speed;
	}
	//=============================================================================================

	//=============================================================================================
	public void addMark(float dT) {
		mark(this.mark += dT);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
