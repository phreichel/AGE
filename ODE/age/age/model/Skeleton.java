//*************************************************************************************************
package age.model;
//*************************************************************************************************

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

//*************************************************************************************************
public class Skeleton {

	//=============================================================================================
	private String name;
	//=============================================================================================

	//=============================================================================================
	private List<Bone> roots = new ArrayList<>();
	private List<Bone> roots_ro = Collections.unmodifiableList(roots);
	//=============================================================================================
	
	//=============================================================================================
	private List<Bone> bones = new ArrayList<>();
	private List<Bone> bones_ro = Collections.unmodifiableList(bones);
	//=============================================================================================
	
	//=============================================================================================
	public String name() {
		return name;
	}
	//=============================================================================================

	//=============================================================================================
	public List<Bone> roots() {
		return roots_ro;
	}
	//=============================================================================================
	
	//=============================================================================================
	public List<Bone> bones() {
		return bones_ro;
	}
	//=============================================================================================
	
}
