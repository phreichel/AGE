//*************************************************************************************************
package age.model;

import java.util.ArrayList;
import java.util.Collections;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

//*************************************************************************************************

//*************************************************************************************************
public class Animation {

	//=============================================================================================
	private String name;
	//=============================================================================================
	
	//=============================================================================================
	private Skeleton skeleton;
	//=============================================================================================

	//=============================================================================================
	private int steps;
	private float steptime;
	//=============================================================================================

	//=============================================================================================
	private List<Keyframes> keyframes = new ArrayList<>();
	private List<Keyframes> keyframes_ro = Collections.unmodifiableList(keyframes);
	//=============================================================================================

	//=============================================================================================
	private Map<String, Sequence> sequences = new HashMap<>();
	private Map<String, Sequence> sequences_ro = Collections.unmodifiableMap(sequences);
	//=============================================================================================
	
	//=============================================================================================
	public String name() {
		return name;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Skeleton skeleton() {
		return skeleton;
	}
	//=============================================================================================

	//=============================================================================================
	public int steps() {
		return steps;
	}
	//=============================================================================================

	//=============================================================================================
	public float steptime() {
		return steptime;
	}
	//=============================================================================================

	//=============================================================================================
	public List<Keyframes> keyframes() {
		return keyframes_ro;
	}
	//=============================================================================================

	//=============================================================================================
	public Map<String, Sequence> sequences() {
		return sequences_ro;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
