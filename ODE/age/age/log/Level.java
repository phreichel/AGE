/**************************************************************************************************
 * Enumeration of Log Levels one can choose from that can be enabled/disabled separately
 */
public enum Level {

	/**********************************************************************************************
	 * Error level literal
	 */
	ERROR,

	/**********************************************************************************************
	 * Warning level literal
	 */
	WARNING,

	/**********************************************************************************************
	 * Info level literal
	 */
	INFO,

	/**********************************************************************************************
	 * Debug level literal
	 */
	DEBUG

}
