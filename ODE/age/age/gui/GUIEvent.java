//*************************************************************************************************
package age.gui;
//*************************************************************************************************

import age.input.InputEvent;

/**************************************************************************************************
 * 
 */
public class GUIEvent extends InputEvent {

	/**********************************************************************************************
	 * 
	 */
	private Widget source;

	/**********************************************************************************************
	 * 
	 * @param source
	 * @param event
	 */
	private GUIEvent(Widget source, InputEvent event) {
		this.source = source;
		set(event);
	}

	/**********************************************************************************************
	 * 
	 */
	public Widget source() {
		return this.source;
	}

}
