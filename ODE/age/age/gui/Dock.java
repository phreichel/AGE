//*************************************************************************************************
package age.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Dock {

	//=============================================================================================
	private float left = 0;
	private float right = 0;
	private float top = 0;
	private float bottom = 0;
	//=============================================================================================

	//=============================================================================================
	public float top() {
		return top;
	}
	//=============================================================================================

	//=============================================================================================
	public float bottom() {
		return bottom;
	}
	//=============================================================================================

	//=============================================================================================
	public float left() {
		return left;
	}
	//=============================================================================================

	//=============================================================================================
	public float right() {
		return right;
	}
	//=============================================================================================

	//=============================================================================================
	public void set(Dock dock) {
		this.left = dock.left;
		this.right = dock.right;
		this.top = dock.top;
		this.bottom = dock.bottom;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void set(float left, float right, float top, float bottom) {
		this.left   = left;
		this.right  = right;
		this.top    = top;
		this.bottom = bottom;
	}
	//=============================================================================================
	
}
