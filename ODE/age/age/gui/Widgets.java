//*************************************************************************************************
package age.gui;

import age.event.Events;
import age.port.Graphics;
import age.port.Renderable;

//*************************************************************************************************

//*************************************************************************************************
public class Widgets implements Renderable {

	//=============================================================================================
	private Handler handler = new Handler(this);
	private Widget root = new Widget();
	//=============================================================================================
	
	//=============================================================================================
	public void assign(Events events) {
		handler.assign(events);
	}
	//=============================================================================================

	//=============================================================================================
	public void render(Graphics g) {
		g.mode2D();
		root.render(g);
	}
	//=============================================================================================

	//=============================================================================================
	public Widget root() {
		return root;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
