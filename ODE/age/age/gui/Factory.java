package age.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Factory {

	//=============================================================================================
	public Widget createButton(
			String image,
			String command) {
		Widget button = new Widget(Flag.BUTTON);
		button.position(5, 5);
		button.dimension(20, 20);
		button.image(image);
		button.command(command);
		return button;
	}
	//=============================================================================================
	
}
